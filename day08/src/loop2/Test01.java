package loop2;
//import java.lang.*;
public class Test01 {
	public static void main(String[] args) {
		
//	for를 이용하여 1부터 10까지 출력
		for(int i=1; i<=10; i++) {
			System.out.println("for=" + i);
		}
		
//	while 반복
		int n =1;
		while(n<=10) {
			System.out.println("while=" + n);
			n++;
		}
		
	}

}
